package java_temperature;

public class TemperatureConversion {

	public static void main(String[] args) {
		
		// Converting Double into int
		double Temperature = 36.7;
		int ConvertedTemperature = (int) Temperature;
		System.out.println("Value of converted Temperature is ---> "+ConvertedTemperature);
		System.out.println("Original Value of the Temperature is ---> "+Temperature);
		
	}

}
